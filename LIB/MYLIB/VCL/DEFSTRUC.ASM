;------------------------------------------------------------------------------
;                       СИСТЕМА КОМПОНЕНТОВ ИНТЕРФЕЙСА 
;------------------------------------------------------------------------------
;
; Каждый визуальный элемент реализован в виде структуры, имитирующей наследование
; и инкапсуляцию в стиле ООП. Базовая структура — UIElement. Остальные компоненты
; наследуют её, добавляя собственные поля и методы.
;
;------------------------------------------------------------------------------

;------------------------------[ UIElement ]-----------------------------------
; Базовая структура для всех элементов интерфейса.
; Содержит координаты, размеры и основной цвет.
;
struct UIElement
    x       dw ?    ; Координата X 
    y       dw ?    ; Координата Y
    wt      dw ?    ; Ширина (в пикселях)
    ht      dw ?    ; Высота (в пикселях)
    color   dw ?    ; Основной цвет (фон/заливка)
ends

;------------------------------[ Panel ]---------------------------------------
; Панель — контейнер для других компонентов. Не содержит текста.
;
struct Panel UIElement
    frame   dw ?    ; Параметры рамки 
    draw    dw ?    ; Указатель на процедуру отрисовки
ends

;------------------------------[ Label ]---------------------------------------
; Метка — компонент только для отображения текста.
;
struct Label UIElement
    frame       dw ?    ; Параметры рамки 
    text        dw ?    ; Указатель на строку текста
    text_color  dw ?    ; Цвет текста
    draw        dw ?    ; Процедура отрисовки метки
ends

;------------------------------[ Edit ]----------------------------------------
; Поле ввода — редактируемый текст, реагирует на клики.
;
struct Edit UIElement
    frame       dw ?    ; Параметры рамки
    text        dw ?    ; Указатель на редактируемый текст
    text_color  dw ?    ; Цвет текста
    draw        dw ?    ; Процедура отрисовки
    on_click    dw ?    ; Обработчик события клика
ends

;------------------------------[ Button ]--------------------------------------
; Кнопка — интерактивный элемент с текстом, состоянием
; и обработчиком нажатия. Используется для запуска действий по клику.
;
struct Button UIElement
    frame       dw ?    ; Параметры рамки
    text        dw ?    ; Указатель на строку текста
    text_color  dw ?    ; Цвет текста
    state       dw ?    ; Состояние (0 = обычное, 1 = нажато) НЕ ИСПОЛЬЗУЕТСЯ
    draw        dw ?    ; Процедура отрисовки кнопки
    on_click    dw ?    ; Обработчик события нажатия
ends

;------------------------------[ Grid ]----------------------------------------
; Сетка — компонент для отображения игрового поля.
;
struct Grid UIElement
    frame               dw ?    ; Параметры рамки
    cols                dw ?    ; Количество колонок
    rows                dw ?    ; Количество строк
    cell_size           dw ?    ; Размер одной ячейки (ширина = высота)
    resize_cell         dw ?    ; Функция масштабирования ячеек
    draw                dw ?    ; Процедура отрисовки всей сетки(НЕ ЯЧЕЕК)
    open_cell           dw ?    ; Рекурсивное открытие ячеек
    fill_grid           dw ?    ; Заполнение ячеек
    update_dimensions   dw ?    ; Перерасчёт размеров и позиций
    on_click            dw ?    ; Обработчик клика по сетке
ends

;------------------------------[ Form ]----------------------------------------
; Форма — главный контейнер. Может содержать любые другие компоненты.
;
struct Form UIElement
    bw              dw ?    ; Толщина рамки
    border_color    dw ?    ; Цвет рамки
    draw            dw ?    ; Процедура отрисовки формы
ends

;------------------------------[ Clickable ]-----------------------------------
; Вспомогательная структура для хранения интерактивных (кликабельных) зон.
;
struct Clickable
    x       dw ?    ; X-координата зоны
    y       dw ?    ; Y-координата зоны
    wt      dw ?    ; Ширина зоны
    ht      dw ?    ; Высота зоны
    handler dw ?    ; Указатель на обработчик клика
ends
